#!/usr/bin/env node
// SessionStart hook — surfaces DESIGN_SYNC.md / .dc.html changes that happened
// since this repo was last opened in Claude Code, so a new session sees what
// needs auditing instead of relying on someone remembering to check.
//
// Deliberately quiet: it only reports what's NEW since the last session, not
// the full backlog every time (DESIGN_SYNC.md has 100+ historical rows —
// dumping all pending ones on every session start would be pure noise).
//
// State (last-seen commit SHA) is stored in .claude/design-sync-state.json,
// which is gitignored — it's per-checkout bookkeeping, not project data.

import { execSync } from 'node:child_process';
import { existsSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..', '..');
const stateFile = join(repoRoot, '.claude', 'design-sync-state.json');

function sh(cmd) {
  try {
    return execSync(cmd, { cwd: repoRoot, encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
}

function readState() {
  if (!existsSync(stateFile)) return {};
  try {
    return JSON.parse(readFileSync(stateFile, 'utf8'));
  } catch {
    return {};
  }
}

function writeState(state) {
  mkdirSync(dirname(stateFile), { recursive: true });
  writeFileSync(stateFile, JSON.stringify(state, null, 2));
}

// Skill reference templates (.claude/skills/ihype-design/templates/*.dc.html)
// are generic examples bundled with the ihype-design skill, not live
// per-page design files — exclude them so they don't create false-positive
// noise every session.
const isRealDcFile = (f) => f.endsWith('.dc.html') && !f.startsWith('.claude/skills/');

const currentSha = sh('git rev-parse HEAD');
if (!currentSha) process.exit(0); // not a git repo / no commits yet

const state = readState();
const lastSha = state.lastSeenSha;
const haveBaseline = Boolean(lastSha) && sh(`git cat-file -e ${lastSha} 2>/dev/null || echo missing`) !== 'missing';

const lines = [];

if (haveBaseline && lastSha !== currentSha) {
  // 1. Real per-page .dc.html files that changed since last session.
  const changedDc = sh(`git diff --name-only ${lastSha} ${currentSha} -- '*.dc.html'`)
    .split('\n')
    .filter(isRealDcFile);
  if (changedDc.length > 0) {
    lines.push(`.dc.html files changed since this repo was last opened (${lastSha.slice(0, 7)} → ${currentSha.slice(0, 7)}):`);
    lines.push(changedDc.map((f) => `  - ${f}`).join('\n'));
  }

  // 2. Newly-added rows in DESIGN_SYNC.md's changelog (added lines only —
  // not the whole backlog) since last session.
  const syncChanged = sh(`git diff --name-only ${lastSha} ${currentSha} -- DESIGN_SYNC.md`);
  if (syncChanged) {
    const addedRows = sh(`git diff -U0 ${lastSha} ${currentSha} -- DESIGN_SYNC.md`)
      .split('\n')
      .filter((l) => l.startsWith('+|') && !l.startsWith('+++'));
    if (addedRows.length > 0) {
      lines.push(`New/updated rows added to DESIGN_SYNC.md since last session:`);
      lines.push(addedRows.map((l) => `  ${l.slice(1)}`).join('\n'));
    }
  }
} else if (!haveBaseline) {
  const dcFiles = sh("git ls-files '*.dc.html'").split('\n').filter(isRealDcFile);
  const pendingCount = existsSync(join(repoRoot, 'DESIGN_SYNC.md'))
    ? readFileSync(join(repoRoot, 'DESIGN_SYNC.md'), 'utf8')
        .split('\n')
        .filter((l) => l.startsWith('|') && l.includes('⏳ Pending')).length
    : 0;
  lines.push('First session tracking design-sync state in this checkout — no prior commit to diff against.');
  lines.push(`Real per-page .dc.html files tracked in repo: ${dcFiles.length}. DESIGN_SYNC.md has ${pendingCount} row(s) marked ⏳ Pending (see the PENDING CHANGES table for the backlog).`);
}

writeState({ lastSeenSha: currentSha, checkedAt: new Date().toISOString() });

if (lines.length > 0) {
  const additionalContext = [
    'DESIGN SYNC AUDIT (auto-generated by .claude/hooks/design-sync-audit.mjs):',
    ...lines,
    '',
    "If any of the above are relevant to this session's task, reconcile the live .tsx implementation against the referenced .dc.html or DESIGN_SYNC.md row before making unrelated changes.",
  ].join('\n');

  console.log(JSON.stringify({
    hookSpecificOutput: {
      hookEventName: 'SessionStart',
      additionalContext,
    },
  }));
}
